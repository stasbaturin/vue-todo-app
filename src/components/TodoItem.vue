<template>
    <li>
        <span v-bind:class="{done: todo.completed}">
            <input type="checkbox" v-model="todo.completed">
            <strong>{{index + 1}}</strong>
            {{todo.title | uppercase}}
        </span>
        <button class="rm"
            v-on:click="$emit('remove-item', todo.id)"
            >&times;</button>
    </li>
</template>

<script>
export default {
    props: {
        todo: {
            type: Object,
            required: true
        },
        index: Number
    },
    filters: {
        uppercase(value) {
            return value.toUpperCase()
        }
    }
}
</script>

